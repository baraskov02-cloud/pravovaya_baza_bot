import random

GOOD_QUOTES = [
    "🇦🇲 {name} — голос непокорённого Арарата",
    "🗣️ {name} говорит как древний царь",
    "🎙️ {name} — лучший оратор Правовой Базы",
    "🦅 {name} — орёл над политикой",
    "🏛️ {name} построил бы идеальный парламент",
    "🎤 {name} разбивает оппонентов",
    "⚖️ {name} — живой символ правосудия",
    "🌍 {name} — гражданин мира",
    "💬 {name} заставит слушать даже стену",
    "🧠 {name} — стратег международного уровня",
    "🔥 {name} может зажечь мёртвый зал",
    "💪 {name} силён духом как весь Кавказ",
]

BAD_QUOTES = [
    "🇦🇲 {name} — родина там, где флаг дешевле",
    "🗣️ {name} говорит громко, но не умно",
    "🎙️ {name} — микрофон, который не выключают",
    "🦅 {name} — цыплёнок, думающий что он орёл",
    "🏛️ {name} построил бы сенат из картона",
    "🎤 {name} — мастер пустозвонства",
    "🌍 {name} — гражданин мира, которого нигде не ждали",
    "💬 {name} говорит так много, что эхо устало",
    "🧠 у {name} вместо мозга — политический навоз",
    "🔥 {name} разжигает дебаты как костёр",
    "🎭 {name} — клоун, которого не звали",
    "🎪 {name} — балаганщик, сам не верит",
]

def get_quote(name, quote_type='random'):
    if quote_type == 'good':
        base = GOOD_QUOTES
    elif quote_type == 'bad':
        base = BAD_QUOTES
    else:
        base = random.choice([GOOD_QUOTES, BAD_QUOTES])
    
    quote = random.choice(base)
    return quote.format(name=name)